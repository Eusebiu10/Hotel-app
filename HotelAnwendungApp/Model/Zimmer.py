class Zimmer:

    def __init__(self):
        pass

    # Setters
    def set_nummer(self, nummer):
        self.nummer = nummer

    def set_anzahl(self, anzahl):
        self.anzahl = anzahl

    def set_preis(self, preis):
        self.preis = preis

    def set_farbe(self, farbe):
        self.farbe = farbe

    def set_meerblick(self, meerblick):
        self.meerblick = meerblick

    # Getters
    def get_nummer(self):
        return self.nummer

    def get_anzahl(self):
        return self.anzahl

    def get_preis(self):
        return self.preis

    def get_farbe(self):
        return self.farbe

    def get_meerblick(self):
        return self.meerblick
